def astar(G, source, target, weight="travel_time"):
    t_lat = G.nodes[target]["y"]
    t_lon = G.nodes[target]["x"]
    max_speed_ms = 130 / 3.6

    def h(node):
        return haversine(G.nodes[node]["y"], G.nodes[node]["x"], t_lat, t_lon) / max_speed_ms

    g_score = {source: 0.0}
    prev = {source: None}
    visited = set()
    heap = [(h(source), source)]
    t0 = time.perf_counter()

    while heap:
        _, u = heapq.heappop(heap)
        if u in visited:
            continue
        visited.add(u)
        if u == target:
            break
        for v, w in get_successors(G, u, weight):
            tentative_g = g_score[u] + w
            if tentative_g < g_score.get(v, math.inf):
                g_score[v] = tentative_g
                prev[v] = u
                heapq.heappush(heap, (tentative_g + h(v), v))

    path = reconstruct(prev, source, target)
    duration = time.perf_counter() - t0
    return path, g_score.get(target, -1), len(visited), duration

