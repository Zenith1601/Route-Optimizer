def dijkstra(G, source, target, weight="travel_time"):
    dist = {source: 0.0}
    prev = {source: None}
    visited = set()
    heap = [(0.0, source)]
    t0 = time.perf_counter()

    while heap:
        cost, u = heapq.heappop(heap)
        if u in visited:
            continue
        visited.add(u)
        if u == target:
            break
        for v, w in get_successors(G, u, weight):
            new_cost = cost + w
            if new_cost < dist.get(v, math.inf):
                dist[v] = new_cost
                prev[v] = u
                heapq.heappush(heap, (new_cost, v))

    path = reconstruct(prev, source, target)
    duration = time.perf_counter() - t0
    return path, dist.get(target, -1), len(visited), duration

